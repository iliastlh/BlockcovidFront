<div id ="hotel">
<h3>Hôtels et autres types d’hébergements</h3>
         <p class="font-weight-normal">
Pour les activités horeca autorisées, au minimum les modalités spécifiques suivantes s'appliquent à l'accueil des clients :
<ul>
<li>les tables sont disposées de manière à garantir une distance d'au moins 1,5 m entre les tablées, sauf si les tables sont séparées par une paroi en plexiglas ou une alternative équivalente, d'une hauteur minimale de 1,8 m</li>
<li>un maximum de 4 personnes par table est autorisé (sauf si toutes les personnes font partie du même ménage)</li>
<li>seules des places assises à table sont autorisées</li>
<li>chaque personne doit rester assise à sa propre table</li>
<li>le port du masque ou, si cela est impossible pour des raisons médicales, d'un écran facial est obligatoire pour le personnel</li>
<li>aucun service au bar n'est autorisé</li>
<li>les données de contact qui peuvent se limiter à un numéro de téléphone ou à une adresse e-mail d'un client par table, sont enregistrées à l'arrivée.</li>
<li>Elles sont conservées, dans le respect de la protection des données à caractère personnel, pendant 14 jours calendrier afin de faciliter toute recherche de contact ultérieure. Les clients qui refusent de laisser leurs coordonnées se voient refuser l'accès à l'établissement à l'arrivée. Ces données de contact ne peuvent être utilisées à d'autres fins que la lutte contre la Covid-19 et elles doivent être détruites après 14 jours calendrier (voir modalités ci-dessous)</li>
</ul>
Les cuisines des hôtels peuvent rester ouvertes pour la fourniture de repas à emporter ou à consommer en chambre (room service).

Les hébergements touristiques (Airbnb, gîtes…) peuvent ouvrir dans le respect des protocoles applicables, à l’exclusion de leur restaurant, de leurs débits de boisson et de leurs facilités communes. 
<br>
En ce qui concerne le nombre de convives par unité d’habitation, les mêmes règles que pour les rassemblements privés à domicile s’appliquent.

Les villages de vacances, les parcs de bungalows et les campings sont fermés au public, à l'exception des hébergements de vacances, des bungalows, des chalets et des commodités pour camper qui servent à l'usage du propriétaire et/ou de son ménage, ou d'un ménage qui y a sa résidence habituelle, et uniquement pour cet usage.

Les discothèques et dancings de ces hébergements doivent, à ce stade, rester fermés.
         
         
